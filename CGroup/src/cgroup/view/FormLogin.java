    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        ControllerAdmin ca = new ControllerAdmin();
        String a = this.txtUsername.getText();
        String b = this.txtPassword.getText();
        boolean status = ca.cekLogin(a, b);
        
        if (status==true){
            //JOptionPane.showMessageDialog(this, "Berhasil Login");
            HomePage hp = new HomePage();
            hp.setVisible(true);
                    
            //DashboardKaryawan dk = new DashboardKaryawan(a);
            //dk.setVisible(true);
            this.setVisible(false);
        }
        else {
            JOptionPane.showMessageDialog(this, "Username/ Password salah");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnLoginActionPerformed

