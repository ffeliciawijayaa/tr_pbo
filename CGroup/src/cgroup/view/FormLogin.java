    private void cb_passActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_passActionPerformed
        if(cb_pass.isSelected()){
            txt_password.setEchoChar((char)0);
        }else{
            txt_password.setEchoChar('*');
        }
    }//GEN-LAST:event_cb_passActionPerformed

    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        ControllerAdmin ca = new ControllerAdmin();
        String a = this.txtUsername.getText();
        String b = this.txtPassword.getText();
        boolean status = ca.cekLogin(a, b);
        
        if (status == true) {
            //JOptionPane.showMessageDialog(this, "Berhasil Login");
            DashboardKaryawan dk = new DashboardKaryawan(a);
            dk.setVisible(true);
            this.setVisible(false);
        }
        else {
            JOptionPane.showMessageDialog(this, "Username/Password salah!");
        }
    }//GEN-LAST:event_btn_loginActionPerformed

