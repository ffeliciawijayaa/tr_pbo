    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String nama = txtNama.getText();
        String alamat = txtAlamat.getText();
        String tanggal_masuk = txtTanggalMasuk.getText();

        String jabatan;
        if (this.rdTetap.isSelected()) {
            jabatan = "Tetap";

        } else {
            jabatan = "Honorer";
        }

        String no_rek = txtNoRekening.getText();
        String email = txtEmail.getText();
        String no_hp = txtNoHP.getText();

        ControllerKaryawan ck = new ControllerKaryawan();
        ck.tambahData(nama, alamat, tanggal_masuk, jabatan, no_rek, email, no_hp);

        boolean status = ck.tambahData(nama, alamat, tanggal_masuk, jabatan, no_rek, email, no_hp);

            if (status == true) {
                JOptionPane.showMessageDialog(this, "Data berhasil di tambahkan!");
                DefaultTableModel dtm = ck.buatTabel();  // Ambil model tabel yang terbaru
                this.tblKaryawan.setModel(dtm);  // Set model baru pada tabel
                ck.tampilkanData();
            }
            else {
                JOptionPane.showMessageDialog(this, "Data gagal di tambahkan!");
            }
    }//GEN-LAST:event_btnTambahActionPerformed

